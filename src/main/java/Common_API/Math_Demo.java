package Common_API;

import static java.lang.Math.floor;

public class Math_Demo {
    public static void main(String[] args) {
        System.out.println(Math.PI);
        System.out.println(Math.abs(-10)); //绝对值
        System.out.println(Math.round(Math.random()*100));//随机和近似值
        System.out.println(Math.sqrt(4)); //平方根
        System.out.println(floor(Math.PI));
    }

}
